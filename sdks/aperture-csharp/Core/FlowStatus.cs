namespace ApertureSDK.Core;

public enum FlowStatus
{
    /// <summary>
    ///     Indicates that the flow completed successfully.
    /// </summary>
    Ok,

    /// <summary>
    ///     Indicates that there was an error in the flow.
    /// </summary>
    Error,

    /// <summary>
    ///     Indicates that the flow status has not been set.
    /// </summary>
    Unset
}
